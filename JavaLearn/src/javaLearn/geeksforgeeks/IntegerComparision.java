//http://www.geeksforgeeks.org/comparison-autoboxed-integer-objects-java/
class IntegerComparision{
	public static void main(String args[]){
		Integer x =400, y =400;
		if(x == y)
			System.out.println("Same");
		else 
			System.out.println("Not Same");
	
		Integer x1 =40, y1 =40;
		if(x1 == y1)
			System.out.println("Same");
		else 
			System.out.println("Not Same");
	}
}